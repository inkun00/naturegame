const BY_ID = new Map(SPECIES.map((s) => [s.id, s]));
const BY_TIER = {
  0: SPECIES.filter((s) => s.tier === 0),
  1: SPECIES.filter((s) => s.tier === 1),
  2: SPECIES.filter((s) => s.tier === 2),
  3: SPECIES.filter((s) => s.tier === 3),
};
const PREY_SET = new Map();
for (const [k, v] of Object.entries(PREY)) PREY_SET.set(Number(k), new Set(v));

// 게임 내 "단계"는 1~4차 소비자(=tier 1~4)까지 존재.
// 단, 종 데이터는 기존처럼 tier 0~3(생산자~3차/최종 소비자)까지만 있으므로
// tier 4(4차 소비자)는 tier 3 최상위 포식자 풀을 재사용한다.
BY_TIER[4] = BY_TIER[3];

const MAX_TIER = 4;

function getSpecies(id) {
  return BY_ID.get(id);
}

function randomSpeciesByTier(tier) {
  const list = BY_TIER[tier] || [];
  if (list.length === 0) return null;
  return list[Math.floor(Math.random() * list.length)];
}

/**
 * predator 가 prey 를 먹을 수 있는가? 
 * 실제 천적관계 기반 (PREY 테이블 참조).
 */
function canEat(predatorSpeciesId, preySpeciesId) {
  const set = PREY_SET.get(predatorSpeciesId);
  if (!set) return false;
  return set.has(preySpeciesId);
}

function preyListOf(predatorSpeciesId) {
  return PREY[predatorSpeciesId] || [];
}

/** 먹이 점수: 더 상위 생물을 먹으면 포만감이 크게 오른다. */
function nutritionOf(speciesId) {
  const s = BY_ID.get(speciesId);
  if (!s) return 0;
  switch (s.tier) {
    case 0:
      return 14;
    case 1:
      return 30;
    case 2:
      return 50;
    case 3:
      return 85;
    default:
      return 10;
  }
}

const TIER_BASE_RADIUS = [12, 22, 40, 68];

/**
 * 반지름 계산.
 * - species 객체나 id 를 넘기면 tier 기본값 × 종별 체격배수.
 * - tier 숫자만 넘기면 기본값 (구버전 호환).
 */
function radiusOf(speciesOrTier) {
  // species object
  if (speciesOrTier && typeof speciesOrTier === "object") {
    const s = speciesOrTier;
    const base = TIER_BASE_RADIUS[s.tier] ?? 12;
    const mult = SIZE_MULT[s.id] ?? 1.0;
    return Math.max(6, Math.round(base * mult));
  }
  // species id
  if (typeof speciesOrTier === "number" && BY_ID.has(speciesOrTier)) {
    const s = BY_ID.get(speciesOrTier);
    const base = TIER_BASE_RADIUS[s.tier] ?? 12;
    const mult = SIZE_MULT[s.id] ?? 1.0;
    return Math.max(6, Math.round(base * mult));
  }
  // fallback: tier 숫자
  return TIER_BASE_RADIUS[speciesOrTier] ?? 12;
}

/** tier 별 이동 속도 (world unit / sec). tier 0 생산자는 0. */
function speedOf(tier) {
  // 전 캐릭터 이동속도 30% 감소 (원본: 220 / 200 / 180)
  // tier 4(4차 소비자)는 최상위 단계이므로 약간 더 빠르게.
  const table = [0, 154, 140, 126, 132];
  return table[tier] ?? 0;
}

